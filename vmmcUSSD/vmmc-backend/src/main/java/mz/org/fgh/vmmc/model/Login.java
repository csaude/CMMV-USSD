package mz.org.fgh.vmmc.model;

public class Login {
	private String id;
	private String username;
	private String access_token;
	private String token_type;

	public String getId() {
		return id;
	}

	public String getUsername() {
		return username;
	}

	public String getAccess_token() {
		return access_token;
	}

	public String getToken_type() {
		return token_type;
	}

}
